static uint32_t crc_buf(const uint32_t *ctx,
                        uint32_t crc,
                        const uint8_t *pin,
                        size_t len) {

  const uint8_t *pend = pin + len;

  if(!ctx[256]) {
    while(pin < pend - 3){
      crc ^= le2me_32(*(const uint32_t*) pin);
      pin+=4;      
      crc =  ctx[768 + (crc & 0xff)] ^ctx[512 + ((crc >> 8 ) &0xff)]
            ^ctx[256 + ((crc >> 16) & 0xff)] ^ctx[((crc >> 24) )];
    }
  }

  while(pin < pend) {
    crc = ctx[ ((uint8_t)crc) ^ *pin++ ] ^ (crc >> 8);
  }

  return crc;
}


uint32_t mp2ts_crc(const unsigned char *buf, unsigned int len) {
  return crc_buf(mpeg_crc_table[0], -1, buf, len);
}

uint32_t ituv42_crc32(const unsigned char *buf, unsigned int len) {
  uint32_t crc = 0xffffffff;
  unsigned int idx;

  for(idx = 0; idx < len; idx++){
    crc = (crc >> 8) ^ (ituv42_crc_table[0][(crc & 0xff) ^ buf[idx]]);
  }

  return (crc ^ 0xffffffff);
}


